total = 0
for window, items in all_data.items():
    count = 0
    for title, source, url, pub_date, cat, importance, summary in items:
        try:
            conn.execute(
                """INSERT OR IGNORE INTO articles
                   (title, source, source_type, url, publish_date, collected_at, category, summary, window, importance, overview)
                   VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)""",
                (title, source, "media", url, pub_date, now, cat, summary, window, importance, title[:35]+"...")
            )
            if conn.total_changes:
                count += 1
        except Exception as e:
            print(f"  skip: {e}")
    conn.commit()
    total += count
    print(f"  window={window}: {count}条")

